const SM_EDITOR_FIELDS = [
  { key: "climate_entity",      label: "Climate Entity (Temperatur)" },
  { key: "pump1_entity",        label: "Düse 1 (switch)" },
  { key: "pump2_entity",        label: "Düse 2 (switch)" },
  { key: "blower_entity",       label: "Gebläse (switch)" },
  { key: "inner_light_entity",  label: "Innenlicht (light)" },
  { key: "outer_light_entity",  label: "Außenlicht (light)" },
  { key: "heating_entity",      label: "Heizstatus (binary_sensor)" },
  { key: "circ_pump_entity",    label: "Zirkulationspumpe (binary_sensor)" },
  { key: "heat_mode_entity",    label: "Heizmodus (select)" },
];

class SundanceMarinCardEditor extends HTMLElement {
  constructor() {
    super();
    this._config = {};
    this._hass = null;
  }

  set hass(h) { this._hass = h; }

  setConfig(config) {
    this._config = { ...SM_DEFAULTS, ...config };
    this._render();
  }

  _render() {
    this.innerHTML = `
<style>
  .editor { padding: 8px 0; }
  .field { display: flex; flex-direction: column; gap: 4px; margin-bottom: 12px; }
  .field-label { font-size: .8rem; color: var(--secondary-text-color); font-weight: 500; }
  .field-input {
    width: 100%; padding: 7px 10px; border-radius: 6px;
    border: 1px solid var(--divider-color);
    background: var(--secondary-background-color);
    color: var(--primary-text-color); font-size: .85rem;
    font-family: monospace;
  }
  .field-input:focus { outline: 2px solid var(--primary-color); border-color: transparent; }
</style>
<div class="editor">
  ${SM_EDITOR_FIELDS.map((f) => `
    <div class="field">
      <label class="field-label">${_esc(f.label)}</label>
      <input class="field-input" data-key="${_esc(f.key)}"
             value="${_esc(this._config[f.key] ?? SM_DEFAULTS[f.key] ?? "")}"
             placeholder="${_esc(SM_DEFAULTS[f.key] ?? "")}">
    </div>
  `).join("")}
</div>`;

    this.querySelectorAll(".field-input").forEach((inp) => {
      inp.addEventListener("change", (e) => {
        const key = e.target.dataset.key;
        const val = e.target.value.trim() || SM_DEFAULTS[key] || "";
        this._config = { ...this._config, [key]: val };
        this.dispatchEvent(new CustomEvent("config-changed", {
          detail: { config: this._config },
          bubbles: true,
          composed: true,
        }));
      });
    });
  }
}

customElements.define("als-sundance-marin-card-editor", SundanceMarinCardEditor);


// XSS protection for all user-visible strings
const _esc = (s) =>
  String(s ?? "").replace(/[&<>"']/g, (m) => ({"&":"&amp;","<":"&lt;",">":"&gt;",'"':"&quot;","'":"&#39;"}[m]));

// Light effect colors — actual spa light colors, not HA theme colors
